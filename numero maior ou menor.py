"Faça um programa que recebe dois números e mostre qual deles é o maior"

A = input("diga o valor de um numero ai pra colocar em A: ")
B = input("diga o valor de um outro numero ai pra colocar em B: ")

if A > B:
    print("A é maior que B")
elif A == B:
    print("os dois valores sao iguais")
else:
    print("B é maior que A")